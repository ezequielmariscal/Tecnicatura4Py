# Profundizando en los sistemas de numeracion

# Sistema decimal
a = 10
print(f'DECIMAL: {a}')

# Sistema binario
a =0b1010
print(f'BINARIO: {a}')

# Sistema octal
a = 0o12
print(f'OCTAL: {a}')

# Sistema hexadecimal
a = 0xA
print(f'HEXADECIMAL: {a}')

# Base Decimal
a = int('23', 10)
print(f'a = base decimal: {a}')

#Base Binario
a = int('10111', 2)
print(f'a = base binario: {a}')